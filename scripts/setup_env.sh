#!/usr/bin/env bash
set -euo pipefail

PROJECT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
ENV_FILE="$PROJECT_ROOT/.env"
TEMPLATE_FILE="$PROJECT_ROOT/.env.template"
TMP_FILE="$(mktemp)"

cleanup() {
  rm -f "$TMP_FILE"
}
trap cleanup EXIT

if [[ ! -f "$TEMPLATE_FILE" ]]; then
  printf 'Missing template file: %s\n' "$TEMPLATE_FILE" >&2
  exit 1
fi

get_existing_value() {
  local key="$1"
  if [[ -f "$ENV_FILE" ]]; then
    grep -E "^${key}=" "$ENV_FILE" | tail -n1 | cut -d'=' -f2- || true
  fi
}

prompt_value() {
  local key="$1"
  local prompt="$2"
  local default="$3"
  local secret="$4"
  local existing value

  existing="$(get_existing_value "$key")"
  value="$existing"

  if [[ -n "$existing" ]]; then
    read -r -p "$key already exists. Keep current value? [Y/n]: " keep
    if [[ ! "$keep" =~ ^[Nn]$ ]]; then
      printf '%s=%s\n' "$key" "$existing" >> "$TMP_FILE"
      return
    fi
  fi

  if [[ "$secret" == "true" ]]; then
    read -r -s -p "$prompt [$default]: " value
    printf '\n'
  else
    read -r -p "$prompt [$default]: " value
  fi

  if [[ -z "$value" ]]; then
    value="$default"
  fi
  printf '%s=%s\n' "$key" "$value" >> "$TMP_FILE"
}

printf '# Generated by scripts/setup_env.sh on %s\n' "$(date -u '+%Y-%m-%dT%H:%M:%SZ')" > "$TMP_FILE"

prompt_value "QUANT_GPT_ENV" "Environment name" "dev" "false"
